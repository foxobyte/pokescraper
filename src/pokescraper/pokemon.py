class Pokemon:
    def __init__(self, nationalNumber: int, name: str, type: list, hp: int, attack: int, defense: int, spAtk: int, spDef: int, speed: int):
        self.nationalNumber = nationalNumber
        self.name = name
        self.type = type
        self.hp = hp
        self.attack = attack
        self.defense = defense
        self.spAttack = spAtk
        self.spDefense = spDef
        self.speed = speed
